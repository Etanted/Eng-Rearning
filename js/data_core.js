/**
 * 미등록 Day를 플레이스홀더로 채우기
 * 반드시 모든 data_days_*.js 파일 로드 이후 App.init()에서 호출
 */
function initPlaceholders() {
  for (let d = 1; d <= TOTAL_DAYS; d++) {
    if (!VOCA_DATA[d]) {
      VOCA_DATA[d] = {
        theme: DAY_THEMES_200[d - 1] || `Day ${d}`,
        words: []
      };
    }
  }
}

/**
 * 전체 단어 플랫 배열 (검색용)
 * @returns {Array<{day, idx, word, pron, pos, meanings, example, exampleKr}>}
 */
function getAllWords() {
  const result = [];
  for (let d = 1; d <= TOTAL_DAYS; d++) {
    const dayData = VOCA_DATA[d];
    if (!dayData) continue;
    (dayData.words || []).forEach((w, idx) => {
      result.push({ day: d, idx, ...w });
    });
  }
  return result;
}
